public class Car
{
	private String car_brand;
	private String car_model;
	private String car_gas;
	private String car_gasTank;

	// getter
	public String getCar_brand()
	{
		return car_brand;
	}

	// setter
	public void setBrand_name(String naming)
	{
		this.car_brand = naming;
	}

	// getter
	public String getCar_model()
	{
		return car_model;
	}

	// setter
	public void setCar_type(String model)
	{
		this.car_model = model;
	}

	// getter
	public String getCar_gas()
	{
		return car_gas;
	}

	// setter
	public void setCar_milage(String milage)
	{
		this.car_brand = mileage;
	}

	// getter
	public String getCar_gasTank()
	{
		return car_gasTank;
	}

	// setter
	public void setBrand_name(String tank)
	{
		this.car_brand = tank;
	}

}
